package atcoder.hhkb2020;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.io.PrintWriter;
import java.math.BigInteger;
import java.util.Arrays;
import java.util.Random;
import java.util.StringTokenizer;

public final class D {

    private static final int MOD = (int) (1e9 + 7);

    static long mul(long a, long b) {
        return (a * b) % MOD;
    }

    public static void main(String[] args) {
        final FastScanner fs = new FastScanner();
        final PrintWriter pw = new PrintWriter(System.out);
        final int t = fs.nextInt();
        for (int i = 0; i < t; i++) {

            final BigInteger n = new BigInteger(fs.next());
            final BigInteger a = new BigInteger(fs.next());
            final BigInteger b = new BigInteger(fs.next());
            System.out.println("aa" + n.compareTo(a.multiply(BigInteger.valueOf(2))));
            System.out.println("aa" + n.compareTo(b.multiply(BigInteger.valueOf(2))));
            final BigInteger placeA = n.subtract(a).add(BigInteger.ONE).multiply(n.subtract(a).add(BigInteger.ONE));
            final BigInteger placeB = n.subtract(b).add(BigInteger.ONE).multiply(n.subtract(b).add(BigInteger.ONE));
            final BigInteger overlap = a.multiply(a).add(b.multiply(b)).subtract(BigInteger.ONE);
//            pw.println(Math.floorMod(mul(placeA, placeB) - mul(overlap, overlap), MOD));
            pw.println(placeA.multiply(placeB).subtract(overlap.multiply(overlap)).mod(BigInteger.valueOf(MOD)));
        }
        pw.close();
    }

    static final class Utils {
        public static void shuffleSort(int[] arr) {
            shuffle(arr);
            Arrays.sort(arr);
        }

        public static void shuffleSort(long[] arr) {
            shuffle(arr);
            Arrays.sort(arr);
        }

        public static void shuffle(int[] arr) {
            final Random r = new Random();

            for (int i = 0; i <= arr.length - 2; i++) {
                final int j = i + r.nextInt(arr.length - i);
                swap(arr, i, j);
            }
        }

        public static void shuffle(long[] arr) {
            final Random r = new Random();

            for (int i = 0; i <= arr.length - 2; i++) {
                final int j = i + r.nextInt(arr.length - i);
                swap(arr, i, j);
            }
        }

        public static void swap(int[] arr, int i, int j) {
            final int t = arr[i];
            arr[i] = arr[j];
            arr[j] = t;
        }

        public static void swap(long[] arr, int i, int j) {
            final long t = arr[i];
            arr[i] = arr[j];
            arr[j] = t;
        }

        private Utils() {}
    }

    static class FastScanner {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        StringTokenizer st = new StringTokenizer("");

        private String next() {
            while (!st.hasMoreTokens()) {
                try {
                    st = new StringTokenizer(br.readLine());
                } catch (IOException e) {
                    //noinspection CallToPrintStackTrace
                    e.printStackTrace();
                }
            }
            return st.nextToken();
        }

        int nextInt() {
            return Integer.parseInt(next());
        }

        long nextLong() {
            return Long.parseLong(next());
        }

        int[] nextIntArray(int n) {
            final int[] a = new int[n];
            for (int i = 0; i < n; i++) { a[i] = nextInt(); }
            return a;
        }

        long[] nextLongArray(int n) {
            final long[] a = new long[n];
            for (int i = 0; i < n; i++) { a[i] = nextLong(); }
            return a;
        }
    }
}
